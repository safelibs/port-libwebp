    let proba: *mut VP8Proba = &raw mut (*dec).proba_;
    let mut t: ::core::ffi::c_int = 0;
    let mut b: ::core::ffi::c_int = 0;
    let mut c: ::core::ffi::c_int = 0;
    let mut p: ::core::ffi::c_int = 0;
    t = 0 as ::core::ffi::c_int;
    while t < NUM_TYPES as ::core::ffi::c_int {
        b = 0 as ::core::ffi::c_int;
        while b < NUM_BANDS as ::core::ffi::c_int {
            c = 0 as ::core::ffi::c_int;
            while c < NUM_CTX as ::core::ffi::c_int {
                p = 0 as ::core::ffi::c_int;
                while p < NUM_PROBAS as ::core::ffi::c_int {
                    let v: ::core::ffi::c_int = (if VP8GetBit(
                        br,
                        CoeffsUpdateProba[t as usize][b as usize][c as usize][p as usize]
                            as ::core::ffi::c_int,
                    ) != 0
                    {
                        VP8GetValue(br, 8 as ::core::ffi::c_int)
                    } else {
                        CoeffsProba0[t as usize][b as usize][c as usize][p as usize] as uint32_t
                    }) as ::core::ffi::c_int;
                    (*proba).bands_[t as usize][b as usize].probas_[c as usize][p as usize] =
                        v as uint8_t;
                    p += 1;
                }
                c += 1;
            }
            b += 1;
        }
        b = 0 as ::core::ffi::c_int;
        while b < 16 as ::core::ffi::c_int + 1 as ::core::ffi::c_int {
            (*proba).bands_ptr_[t as usize][b as usize] =
                (&raw mut *(&raw mut (*proba).bands_ as *mut [VP8BandProbas; 8]).offset(t as isize)
                    as *mut VP8BandProbas)
                    .offset(*(&raw const kBands as *const uint8_t).offset(b as isize) as isize)
                    as *mut VP8BandProbas;
            b += 1;
        }
        t += 1;
    }
    (*dec).use_skip_proba_ = VP8GetValue(br, 1 as ::core::ffi::c_int) as ::core::ffi::c_int;
    if (*dec).use_skip_proba_ != 0 {
        (*dec).skip_p_ = VP8GetValue(br, 8 as ::core::ffi::c_int) as uint8_t;
    }
