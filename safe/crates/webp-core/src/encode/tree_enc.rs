pub unsafe extern "C" fn VP8WriteProbas(bw: *mut VP8BitWriter, probas: *const VP8EncProba) {
    let mut t: ::core::ffi::c_int = 0;
    let mut b: ::core::ffi::c_int = 0;
    let mut c: ::core::ffi::c_int = 0;
    let mut p: ::core::ffi::c_int = 0;
    t = 0 as ::core::ffi::c_int;
    while t < NUM_TYPES as ::core::ffi::c_int {
        b = 0 as ::core::ffi::c_int;
        while b < NUM_BANDS as ::core::ffi::c_int {
            c = 0 as ::core::ffi::c_int;
            while c < NUM_CTX as ::core::ffi::c_int {
                p = 0 as ::core::ffi::c_int;
                while p < NUM_PROBAS as ::core::ffi::c_int {
                    let p0: uint8_t =
                        (*probas).coeffs_[t as usize][b as usize][c as usize][p as usize];
                    let update: ::core::ffi::c_int = (p0 as ::core::ffi::c_int
                        != VP8CoeffsProba0[t as usize][b as usize][c as usize][p as usize]
                            as ::core::ffi::c_int)
                        as ::core::ffi::c_int;
                    if VP8PutBit(
                        bw,
                        update,
                        VP8CoeffsUpdateProba[t as usize][b as usize][c as usize][p as usize]
                            as ::core::ffi::c_int,
                    ) != 0
                    {
                        VP8PutBits(bw, p0 as uint32_t, 8 as ::core::ffi::c_int);
                    }
                    p += 1;
                }
                c += 1;
            }
            b += 1;
        }
        t += 1;
    }
    if VP8PutBitUniform(bw, (*probas).use_skip_proba_) != 0 {
        VP8PutBits(
            bw,
            (*probas).skip_proba_ as uint32_t,
            8 as ::core::ffi::c_int,
        );
    }
}
